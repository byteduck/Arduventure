void loadRoomKey1(){
  if(!data.gotKey1) {
    objects[0] = {
      .sprite = KEY,
      .x = 54,
      .y = 28,
      .width = 8,
      .height = 4
    };
  }
}

void unloadRoomKey1(){
  objects[0].sprite = nullptr;
}

void updateRoomKey1(){
  if(collides(player, objects[0])) {
    objects[0].sprite = nullptr;
    data.hasKey = true;
    data.gotKey1 = true;
  }
}

